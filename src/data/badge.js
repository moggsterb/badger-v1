


class Badge {

  constructor(shape,style,icon,theme,shuffle,textA,textB) {
    this.shape = shape;
    this.style = style;
    this.icon = icon;
    this.theme = theme;
    this.shuffle = shuffle;
    this.TextA = textA;
    this.TextB = textB;
    this.id = (+new Date() + Math.floor(Math.random() * 9999999)).toString(36);
  }

  shapeData() {
    return MOCK_SHAPES.default[this.shape].variants[this.style].data;
  }

  iconData() {
    return MOCK_ICONS.default[this.icon].data;
  }

  themeData() {
    return MOCK_THEMES.default[this.theme].colors.slice(0);;
  }

  buildShape() {
    var data = this.shapeData().concat(this.iconData());
    var th = this.themeData();
    for (i = 0; i < this.shuffle; i++)
    {
      th.push(th.shift());
    };
    var svg = data.replace(/#000000/g,th[0]).replace(/#FFFFFF/g,th[1]).replace(/#FF0000/g,th[2]).replace(/#00FF00/g,th[3]).replace(/#0000FF/g,th[4]);
    return svg;
  }
}

